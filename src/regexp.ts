export const regexps = {
  /** 匹配 URL 的正则表达式 */
  url: /^(https?|ftp|file|mailto|tel|data|irc|ssh|git|svn|ws|wss):\/\/[^\s/$.?#].[^\s]*$/i,

  /** 匹配邮箱地址的正则表达式 */
  email: /^[^\s@]+@[^\s@]+\.[^\s@]+$/,

  /** 匹配手机号的正则表达式（中国大陆） */
  phone: /^1[3-9]\d{9}$/,

  /** 匹配身份证号码的正则表达式（中国大陆） */
  idCard:
    /^\d{6}(18|19|20)?\d{2}(0[1-9]|1[0-2])(0[1-9]|[12]\d|3[01])\d{3}(\d|X)$/,
};
